#include<bits/stdc++.h>
using namespace std;
int main()
{
    cout<<"floor\n";
    cout<<"for 2.5 : "<<floor(2.5)<<endl;
    cout<<"for 2.3 : "<<floor(2.3)<<endl;
    cout<<"for 2.9 : "<<floor(2.9)<<endl;
    cout<<"for -2.5 : "<<floor(-2.5)<<endl;
    cout<<"for -2.3 : "<<floor(-2.3)<<endl;
    cout<<"for -2.9 : "<<floor(-2.9)<<endl;

    cout<<"Ceil\n";
    cout<<"for 2.5 : "<<ceil(2.5)<<endl;
    cout<<"for 2.3 : "<<ceil(2.3)<<endl;
    cout<<"for 2.9 : "<<ceil(2.9)<<endl;
    cout<<"for -2.5 : "<<ceil(-2.5)<<endl;
    cout<<"for -2.3 : "<<ceil(-2.3)<<endl;
    cout<<"for -2.9 : "<<ceil(-2.9)<<endl;
}
